(defn count-trees
  [input [right down]]
  (->> input
       (keep-indexed #(let [idx (/ %1 down)]
                        (when (int? idx)
                          (get %2 (mod (* idx right)
                                       (count %2))))))
       (filter #{\#})
       count))

(defn day3
  [input]
  (let [lines (clojure.string/split-lines input)
        counts (into {}
                     (map (juxt identity (partial count-trees lines)))
                     [[1 1] [3 1] [5 1] [7 1] [1 2]])]
    (println (counts [3 1]))
    (println (reduce * (vals counts)))))

(day3 input)
